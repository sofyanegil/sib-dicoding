// !Loop
// *for
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// *for of
let computer = ['CPU', 'RAM', 'VGA', 'SSD'];
for (const comp of computer) {
  console.log(comp);
}

// *While
let i = 1;
while (i <= 10) {
  console.log(i);
  i++;
}

do {
  console.log(i);
  i++;
} while (i <= 20);

