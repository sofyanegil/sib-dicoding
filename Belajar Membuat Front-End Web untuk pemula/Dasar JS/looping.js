// !Perulangan
// *for
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// *for of
let myArray = ["Sofyan", "Egi", "Lesmana", "S.T"];

for (const arrayItem of myArray) {
  console.log(arrayItem);
}

// *while and do while
let i = 1;
while (i <= 10) {
  console.log(i);
  i++;
}

let j = 1;
do {
  console.log(j);
  j++;
} while (j <= 2);

// !Infinite loop
let c = 1;
while (c <= 5) {
  console.log(c);
  // c++;
}
